package Section5_Conditional_Statement;

public class Example2_Selection_Statement
{
    // Selection Statement
    // Selection statements in Java are control flow statements that execute specific blocks of code based on whether a condition evaluates to true or false
    // 1) If statement
    // 2) switch Statement
    static void main()
    {
        int AGE = 18;

        if(AGE >= 18)
        {
            System.out.println("YOU CAN VOTE!");
        }

        else
        {
            System.out.println("YOU CAN NOT VOTE !");
        }

    }
}
