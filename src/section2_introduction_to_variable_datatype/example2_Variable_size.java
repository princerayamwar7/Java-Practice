package section2_introduction_to_variable_datatype;

public class example2_Variable_size {
    public static void main(String[] args)
    {
        // Byte  -128 to 127
        System.out.println(Byte.MIN_VALUE);
        System.out.println(Byte.MAX_VALUE);
        System.out.println(Byte.SIZE);
        System.out.println(Byte.BYTES);

        // Short -32k to +32k
        System.out.println(Short.MIN_VALUE);
        System.out.println(Short.MAX_VALUE);
        System.out.println(Short.SIZE);
        System.out.println(Short.BYTES);

        // Integer -2B to +2B
        System.out.println(Integer.MIN_VALUE);
        System.out.println(Integer.MAX_VALUE);
        System.out.println(Integer.SIZE);
        System.out.println(Integer.BYTES);

        // long More than 2B
        System.out.println(Long.MIN_VALUE);
        System.out.println(Long.MAX_VALUE);
        System.out.println(Long.SIZE);
        System.out.println(Long.BYTES);

        // float
        System.out.println(Float.MIN_VALUE);
        System.out.println(Float.MAX_VALUE);
        System.out.println(Float.SIZE);
        System.out.println(Float.BYTES);
        System.out.println(Float.PRECISION);
        System.out.println(Float.MAX_EXPONENT);
        System.out.println(Float.MIN_EXPONENT);

        // Double
        System.out.println(Double.MIN_VALUE);
        System.out.println(Double.MIN_VALUE);
        System.out.println(Double.SIZE);
        System.out.println(Double.BYTES);

        //Character
        System.out.println(Character.MIN_VALUE);
        System.out.println(Character.MAX_VALUE);
        System.out.println(Character.SIZE);
        System.out.println(Character.BYTES);

        // the size of boolean data type depends on java virtual machine.


    }
}
